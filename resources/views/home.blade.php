                    Banjar. Kesultanan Banjar mencapai puncak kejayaannya pada abad ke-17. Pada abad ke-19, Kesultanan
                    Banjar mulai mengalami kemunduran. Hal ini disebabkan oleh berbagai faktor, antara lain serangan
                    dari Kerajaan Belanda, perselisihan internal, dan perubahan sistem pemerintahan. Pada tahun 1859,
                    Pangeran Antasari memimpin perlawanan rakyat Banjar melawan Belanda. Namun, perlawanan ini akhirnya
                    gagal dan Kesultanan Banjar akhirnya runtuh pada tahun 1905.
                    Setelah Kesultanan Banjar runtuh, Kalimantan Selatan kemudian menjadi bagian dari Hindia Belanda.
                    Pada masa penjajahan Belanda, Kalimantan Selatan dibagi menjadi beberapa wilayah administratif,
                    antara lain Afdeling Borneo Selatan, Afdeling Borneo Barat, dan Afdeling Borneo Timur. Pada tahun
                    1942, Jepang menduduki Kalimantan Selatan. Pada masa pendudukan Jepang, Kalimantan Selatan dibagi
                    menjadi beberapa wilayah administratif, antara lain Kalimantan Selatan I, Kalimantan Selatan II, dan
                    Kalimantan Selatan III. Pada tahun 1945, Indonesia memproklamasikan kemerdekaannya. Kalimantan
                    Selatan kemudian menjadi bagian dari Republik Indonesia. Pada tahun 1957, Kalimantan Selatan
                    menjadi provinsi yang berdiri sendiri.
